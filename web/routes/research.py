@router.get("/search")
def search(
    q: str = Query(default=""),
    limit: int = Query(default=20),
    db: list[str] = Query(default=[]),
    type: list[str] = Query(default=[]),
):
    """Semantic search across the research corpus.

    Query params:
        q: search query (required)
        limit: max results (default 20, max 50)
        db: database_key filter (repeatable)
        type: page Type filter (repeatable, e.g. "Verbatim", "Observation")
    """
    q = q.strip()
    if not q:
        return JSONResponse({"error": "Missing query parameter 'q'"}, status_code=400)

    limit = min(limit, 50)
    db_filter = set(db)
    type_filter = set(type)

    conn = _get_db()
    if not conn:
        return JSONResponse({"error": "Research database not found"}, status_code=404)

    try:
        query_vec = _embed_query(q)
        chunks, embeddings = _get_embeddings(conn)
        pages = _get_pages(conn)
        if len(chunks) == 0:
            return {"results": [], "query": q}

        scores = _cosine_scores(query_vec, embeddings)
        top_indices = np.argsort(scores)[::-1]

        # Collect more than needed before dedup
        raw_results = []
        for idx in top_indices:
            if len(raw_results) >= limit * 3:
                break
            chunk = chunks[idx]
            if db_filter and chunk["database_key"] not in db_filter:
                continue
            page_info = pages.get(chunk["page_id"])
            if type_filter and (not page_info or page_info.get("type") not in type_filter):
                continue
            raw_results.append(_build_result(chunk, page_info, scores[idx]))

        results = _dedupe_by_page(raw_results)[:limit]

        return {
            "results": results,
            "query": q,
            "total_chunks": len(chunks),
        }
    finally:
        conn.close()


@router.get("/similar/{chunk_id}")
def similar(chunk_id: int, limit: int = Query(default=15)):
    """Find chunks similar to a given chunk (deduplicated by page)."""
    limit = min(limit, 30)

    conn = _get_db()
    if not conn:
        return JSONResponse({"error": "Research database not found"}, status_code=404)

    try:
        chunks, embeddings = _get_embeddings(conn)
        pages = _get_pages(conn)

        target_idx = None
        for i, c in enumerate(chunks):
            if c["id"] == chunk_id:
                target_idx = i
                break
        if target_idx is None:
            return JSONResponse({"error": "Chunk not found"}, status_code=404)

        source_page_id = chunks[target_idx]["page_id"]
        target_vec = embeddings[target_idx]
        scores = _cosine_scores(target_vec, embeddings)
        top_indices = np.argsort(scores)[::-1]

        raw_results = []
        for idx in top_indices:
            if len(raw_results) >= limit * 3:
                break
            # Skip chunks from the source page
            if chunks[idx]["page_id"] == source_page_id:
                continue
            chunk = chunks[idx]
            page_info = pages.get(chunk["page_id"])
            raw_results.append(_build_result(chunk, page_info, scores[idx]))

        results = _dedupe_by_page(raw_results)[:limit]

        return {
            "results": results,
            "source_chunk_id": chunk_id,
            "source_page_id": source_page_id,
        }
    finally:
        conn.close()


@router.get("/pages/{page_id}")
def page_detail(page_id: str):
    """Full page details: properties, blocks, relations, chunks."""
    conn = _get_db()
    if not conn:
        return JSONResponse({"error": "Research database not found"}, status_code=404)

    try:
        page = conn.execute(
            "SELECT * FROM pages WHERE id = ?", (page_id,)
        ).fetchone()
        if not page:
            return JSONResponse({"error": "Page not found"}, status_code=404)

        props = json.loads(page["properties_json"]) if page["properties_json"] else {}

        blocks = conn.execute(
            "SELECT type, text_content, position FROM blocks "
            "WHERE page_id = ? ORDER BY position",
            (page_id,),
        ).fetchall()

        # Resolved relations
        relations = {}
        rels = conn.execute(
            "SELECT r.property_name, p.title, p.id as target_id "
            "FROM relations r "
            "LEFT JOIN pages p ON p.id = r.target_page_id "
            "WHERE r.source_page_id = ?",
            (page_id,),
        ).fetchall()
        for r in rels:
            prop = r["property_name"]
            if prop not in relations:
                relations[prop] = []
            relations[prop].append({
                "title": r["title"],
                "page_id": r["target_id"],
            })

        chunks = conn.execute(
            "SELECT id, chunk_index FROM chunks "
            "WHERE page_id = ? ORDER BY chunk_index",
            (page_id,),
        ).fetchall()

        return {
            "id": page["id"],
            "title": page["title"],
            "database_key": page["database_key"],
            "database_name": page["database_name"],
            "url": page["url"],
            "type": props.get("Type"),
            "date": props.get("Date"),
            "properties": props,
            "blocks": [
                {"type": b["type"], "text": b["text_content"], "position": b["position"]}
                for b in blocks
            ],
            "relations": relations,
            "chunk_ids": [c["id"] for c in chunks],
        }
    finally:
        conn.close()


@router.get("/stats")
def stats():
    """Corpus statistics, including type breakdown for entretiens."""
    conn = _get_db()
    if not conn:
        return JSONResponse({"error": "Research database not found"}, status_code=404)

    try:
        pages_n = conn.execute("SELECT COUNT(*) as n FROM pages").fetchone()["n"]
        blocks_n = conn.execute("SELECT COUNT(*) as n FROM blocks").fetchone()["n"]
        chunks_n = conn.execute("SELECT COUNT(*) as n FROM chunks").fetchone()["n"]

        db_stats = conn.execute(
            "SELECT database_key, database_name, COUNT(*) as n "
            "FROM pages GROUP BY database_key"
        ).fetchall()

        chunk_counts = conn.execute(
            "SELECT database_key, COUNT(*) as n FROM chunks GROUP BY database_key"
        ).fetchall()
        chunks_by_db = {r["database_key"]: r["n"] for r in chunk_counts}

        # Type breakdown for entretiens
        type_rows = conn.execute(
            "SELECT properties_json FROM pages WHERE database_key = 'entretiens'"
        ).fetchall()
        type_counts = {}
        for row in type_rows:
            props = json.loads(row["properties_json"]) if row["properties_json"] else {}
            t = props.get("Type")
            if t:
                type_counts[t] = type_counts.get(t, 0) + 1

        meta = {}
        for row in conn.execute("SELECT key, value FROM sync_meta").fetchall():
            meta[row["key"]] = row["value"]

        return {
            "pages": pages_n,
            "blocks": blocks_n,
            "chunks": chunks_n,
            "databases": [
                {
                    "key": r["database_key"],
                    "name": r["database_name"],
                    "pages": r["n"],
                    "chunks": chunks_by_db.get(r["database_key"], 0),
                }
                for r in db_stats
            ],
            "types": type_counts,
            "sync_meta": meta,
        }
    finally:
        conn.close()
