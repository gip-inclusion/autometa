"""Research corpus exploration API routes.

Provides semantic search and navigation across the Notion "Connaissance du terrain"
corpus (interviews, observations, themes, hypotheses, conclusions).

Embeddings are stored in data/notion_research.db (generated by scripts/embed_notion_research.py).
Query encoding uses DeepInfra's hosted Qwen3-Embedding-0.6B model.
"""

import json
import logging
import sqlite3
import threading

import numpy as np
import requests
from fastapi import APIRouter, Query
from fastapi.responses import JSONResponse

from .. import config

logger = logging.getLogger(__name__)

router = APIRouter(prefix="/api/research")

DEEPINFRA_URL = "https://api.deepinfra.com/v1/openai/embeddings"
EMBEDDING_MODEL = "Qwen/Qwen3-Embedding-0.6B"

# In-memory cache for embeddings (invalidated when DB file changes)
_cache_lock = threading.Lock()
_cache = {"chunks": None, "embeddings": None, "mtime": 0}

# Page metadata cache (avoids N+1 queries per search)
_page_cache_lock = threading.Lock()
_page_cache = {"pages": None, "mtime": 0}


def _get_db():
    """Get a read-only connection to the research database."""
    db_path = config.NOTION_RESEARCH_DB
    if not db_path.exists():
        return None
    conn = sqlite3.connect(f"file:{db_path}?mode=ro", uri=True)
    conn.row_factory = sqlite3.Row
    return conn


def _blob_to_vec(blob):
    return np.frombuffer(blob, dtype=np.float32)


def _get_embeddings(conn):
    """Load and cache the normalized embeddings matrix."""
    db_path = config.NOTION_RESEARCH_DB
    mtime = db_path.stat().st_mtime

    with _cache_lock:
        if _cache["mtime"] == mtime and _cache["chunks"] is not None:
            return _cache["chunks"], _cache["embeddings"]

    rows = conn.execute(
        "SELECT id, page_id, chunk_index, text, database_key, embedding FROM chunks"
    ).fetchall()

    chunks = []
    vecs = []
    for row in rows:
        chunks.append({
            "id": row["id"],
            "page_id": row["page_id"],
            "chunk_index": row["chunk_index"],
            "text": row["text"],
            "database_key": row["database_key"],
        })
        vecs.append(_blob_to_vec(row["embedding"]))

    if vecs:
        embeddings = np.vstack(vecs)
        norms = np.linalg.norm(embeddings, axis=1, keepdims=True)
        norms[norms == 0] = 1
        embeddings = embeddings / norms
    else:
        embeddings = np.empty((0, 0))

    with _cache_lock:
        _cache.update(chunks=chunks, embeddings=embeddings, mtime=mtime)

    return chunks, embeddings


def _get_pages(conn):
    """Load and cache page metadata (title, type, date, url, database)."""
    db_path = config.NOTION_RESEARCH_DB
    mtime = db_path.stat().st_mtime

    with _page_cache_lock:
        if _page_cache["mtime"] == mtime and _page_cache["pages"] is not None:
            return _page_cache["pages"]

    rows = conn.execute(
        "SELECT id, title, database_key, database_name, url, properties_json FROM pages"
    ).fetchall()

    pages = {}
    for row in rows:
        props = json.loads(row["properties_json"]) if row["properties_json"] else {}
        pages[row["id"]] = {
            "title": row["title"],
            "database_key": row["database_key"],
            "database_name": row["database_name"],
            "url": row["url"],
            "type": props.get("Type"),
            "date": props.get("Date"),
        }

    with _page_cache_lock:
        _page_cache.update(pages=pages, mtime=mtime)

    return pages


def _embed_query(text):
    """Encode a query string using DeepInfra API."""
    api_key = config.DEEPINFRA_API_KEY
    if not api_key:
        raise ValueError("DEEPINFRA_API_KEY not configured")

    resp = requests.post(
        DEEPINFRA_URL,
        headers={
            "Authorization": f"Bearer {api_key}",
            "Content-Type": "application/json",
        },
        json={
            "model": EMBEDDING_MODEL,
            "input": [text],
            "encoding_format": "float",
        },
        timeout=10,
    )
    resp.raise_for_status()
    data = resp.json()
    vec = np.array(data["data"][0]["embedding"], dtype=np.float32)
    vec = vec / np.linalg.norm(vec)
    return vec


def _cosine_scores(query_vec, embeddings):
    """Dot product against pre-normalized matrix."""
    return embeddings @ query_vec


def _extract_body(text):
    """Extract body text from a chunk, stripping the metadata header.

    Chunk format:
      Title
      [Database name]
      Key: Value
      ...
      ---           (only in multi-chunk pages)
      body text

    For single-chunk pages (no ---), the title line and metadata lines
    after it are stripped; any remaining content is the body.
    """
    if "\n---\n" in text:
        return text.split("\n---\n", 1)[1].strip()

    # No --- separator: skip title (line 0) + metadata lines
    lines = text.split("\n")
    body_start = 1  # skip title
    for i in range(1, len(lines)):
        stripped = lines[i].strip()
        if not stripped:
            body_start = i + 1
            continue
        if stripped.startswith("[") and stripped.endswith("]"):
            body_start = i + 1
            continue
        if ": " in stripped and len(stripped) < 200:
            body_start = i + 1
            continue
        # Non-metadata line found
        break
    body = "\n".join(lines[body_start:]).strip()
    return body


def _build_result(chunk, page_info, score=None):
    """Build a result dict from a chunk and its page metadata."""
    result = {
        "chunk_id": chunk["id"],
        "page_id": chunk["page_id"],
        "chunk_index": chunk["chunk_index"],
        "body": _extract_body(chunk["text"]),
        "database_key": chunk["database_key"],
        "page_title": page_info["title"] if page_info else None,
        "database_name": page_info["database_name"] if page_info else None,
        "page_url": page_info["url"] if page_info else None,
        "page_type": page_info["type"] if page_info else None,
        "page_date": page_info["date"] if page_info else None,
    }
    if score is not None:
        result["score"] = round(float(score), 4)
    return result


def _dedupe_by_page(results):
    """Keep only the best-scoring result per page_id."""
    seen = {}
    deduped = []
    for r in results:
        pid = r["page_id"]
        if pid not in seen:
            seen[pid] = len(deduped)
            r["matching_chunks"] = 1
            deduped.append(r)
        else:
            deduped[seen[pid]]["matching_chunks"] += 1
    return deduped


